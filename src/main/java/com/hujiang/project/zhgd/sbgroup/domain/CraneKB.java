package com.hujiang.project.zhgd.sbgroup.domain;

public class CraneKB {
    private int llimit;
    private int rlimit;
    private int flimit;
    private int blimit;
    private int incline;
    private int hoisting;
    private int windSpeed;

    public int getLlimit() {
        return llimit;
    }

    public void setLlimit(int llimit) {
        this.llimit = llimit;
    }

    public int getRlimit() {
        return rlimit;
    }

    public void setRlimit(int rlimit) {
        this.rlimit = rlimit;
    }

    public int getFlimit() {
        return flimit;
    }

    public void setFlimit(int flimit) {
        this.flimit = flimit;
    }

    public int getBlimit() {
        return blimit;
    }

    public void setBlimit(int blimit) {
        this.blimit = blimit;
    }

    public int getIncline() {
        return incline;
    }

    public void setIncline(int incline) {
        this.incline = incline;
    }

    public int getHoisting() {
        return hoisting;
    }

    public void setHoisting(int hoisting) {
        this.hoisting = hoisting;
    }

    public int getWindSpeed() {
        return windSpeed;
    }

    public void setWindSpeed(int windSpeed) {
        this.windSpeed = windSpeed;
    }
}
