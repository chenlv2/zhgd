package com.hujiang.project.zhgd.sbGroup.domain;

public class ElevatorKB {
    private int load;
    private int people;
    private int Xincline;
    private int Yincline;

    public int getLoad() {
        return load;
    }

    public void setLoad(int load) {
        this.load = load;
    }

    public int getPeople() {
        return people;
    }

    public void setPeople(int people) {
        this.people = people;
    }

    public int getXincline() {
        return Xincline;
    }

    public void setXincline(int xincline) {
        Xincline = xincline;
    }

    public int getYincline() {
        return Yincline;
    }

    public void setYincline(int yincline) {
        Yincline = yincline;
    }
}
